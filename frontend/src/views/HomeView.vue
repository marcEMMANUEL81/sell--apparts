<script>
import { mapState } from 'vuex';
export default {
    name: 'HomeView',
    data() {
        return{
            allApparts: [],
            OneAppart: {},
            ALL_MY_APPARTS: [],
            selected: "",
            dataVersion: 0
        } 
    },

    computed: {
        ...mapState(['Oneappart']),
    },

    methods: {

        async getList() {
            let token = localStorage.getItem('token');
            const _url = 'http://192.168.252.170:3001/api/apparts/getApparts';
            let _datas = {};
            await fetch(_url, {method: 'GET', headers: {'Content-Type': 'application/json', 'Access-Control-Allow-Origin': '*', 'Authorization': 'Bearer ' + token }})
            .then((response) => response.json())
            .then((response) => {
                _datas = response.results;
                this.dataVersion ++;
            })
            .catch((error) => {
                console.log('ERROR : ', error);
            })
            return _datas;
        },

        filterData() {
            let option = document.querySelector("select").value;
            console.log(option)
            switch(option) {
                case "0":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["type"] == "Maison");
                    break;
                case "1":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["type"] == "Appartement");
                    break;
                case "2":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["type"] == "Magasin");
                    break;
                case "3":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["type"] == "Espace Evênementiel");
                    break;
                case "4":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["vendu"] === 0);
                    break;
                case "5":
                    this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["vendu"] == 1);
                    break;
                default:
                    this.allApparts = this.ALL_MY_APPARTS
            }
        },

        filterByPrice() {
            let priceInput = document.querySelectorAll(".price-input input");
            let minPrice = parseInt(priceInput[0].value);
            let maxPrice = parseInt(priceInput[1].value);
            this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["prix"] > minPrice && appart["prix"] < maxPrice);
        },

        filterByName() {
            let entry = document.querySelector('#entry').value
            this.allApparts = this.ALL_MY_APPARTS.filter(appart => appart["titre"].includes(entry));
        }

    },

    async created() {

        if (!localStorage.getItem('token')){
            this.$router.push({ name: 'signIn' });
        }

        this.allApparts = await this.getList();
        this.ALL_MY_APPARTS = this.allApparts;

        const rangeInput = document.querySelectorAll(".range-input input");
        let priceInput = document.querySelectorAll(".price-input input");
        let range = document.querySelector(".slider .progress");
        let priceGap = 1000;
        priceInput.forEach(input =>{
            input.addEventListener("input", e =>{
                let minPrice = parseInt(priceInput[0].value);
                let maxPrice = parseInt(priceInput[1].value);
        
                if((maxPrice - minPrice >= priceGap) && maxPrice <= rangeInput[1].max){
                    if(e.target.className === "input-min"){
                        rangeInput[0].value = minPrice;
                        range.style.left = ((minPrice / rangeInput[0].max) * 100) + "%";
                    }else{
                        rangeInput[1].value = maxPrice;
                        range.style.right = 100 - (maxPrice / rangeInput[1].max) * 100 + "%";
                    }
                }
            });
        });
    
        rangeInput.forEach(input =>{
            input.addEventListener("input", e =>{
                let minVal = parseInt(rangeInput[0].value);
                let maxVal = parseInt(rangeInput[1].value);
                if((maxVal - minVal) < priceGap){
                    if(e.target.className === "range-min"){
                        rangeInput[0].value = maxVal - priceGap
                    }else{
                        rangeInput[1].value = minVal + priceGap;
                    }   
                }else{
                    priceInput[0].value = minVal;
                    priceInput[1].value = maxVal;
                    range.style.left = ((minVal / rangeInput[0].max) * 100) + "%";
                    range.style.right = 100 - (maxVal / rangeInput[1].max) * 100 + "%";
                }
            });
        });
    },
    
}
